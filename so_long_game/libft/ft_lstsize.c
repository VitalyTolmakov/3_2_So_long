#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		i;

	if (lst != NULL)
	{
		i = 1;
		while (lst->next)
		{
			i++;
			lst = lst->next;
		}
		return (i);
	}
	return (0);
}
